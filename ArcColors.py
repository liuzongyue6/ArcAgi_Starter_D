import numpy as np
from matplotlib.colors import ListedColormap

# adapted directly from the arc-py project
hex_colors = (
    (0x00, 0x00, 0x00),
    (0x00, 0x74, 0xD9),
    (0xFF, 0x41, 0x36),
    (0x2E, 0xCC, 0x40),
    (0xFF, 0xDC, 0x00),
    (0xA0, 0xA0, 0xA0),
    (0xF0, 0x12, 0xBE),
    (0xFF, 0x85, 0x1B),
    (0x7F, 0xDB, 0xFF),
    (0x87, 0x0C, 0x25),
)

colors = np.asarray([tuple(value / 255 for value in color) for color in hex_colors])
arc_colors = ListedColormap(colors)
